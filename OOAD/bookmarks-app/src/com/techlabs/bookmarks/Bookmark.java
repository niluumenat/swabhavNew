package com.techlabs.bookmarks;

public class Bookmark {
	private String url;
	private String description;
	public Bookmark(String url, String description) {
		super();
		this.url = url;
		this.description = description;
	}
	public String getUrl() {
		return url;
	}
	public String getDescription() {
		return description;
	}
	
	
	
	

}
